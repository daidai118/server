type Codec struct {
	PrivateKey byte
	PublicKey  byte
	Table      [256]byte
}

func New(privateKey, publicKey byte, table [256]byte) Codec {
	return Codec{PrivateKey: privateKey, PublicKey: publicKey, Table: table}
}

func NewPythonReference(privateKey, publicKey byte) Codec {
	return New(privateKey, publicKey, PythonReferenceTable)
}

func (c Codec) Transform(data []byte) []byte {
	out := make([]byte, len(data))
	for i, b := range data {
		out[i] = c.PrivateKey ^ c.Table[i%len(c.Table)] ^ b ^ c.PublicKey
	}
	return out
}
